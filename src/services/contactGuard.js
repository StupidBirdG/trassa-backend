// Детектор обмена контактами в чате (2026-07-15) — часть анти-дезинтермедиации:
// если стороны обмениваются номером/мессенджером в чате по грузу, есть риск, что
// сделка после первого раза уйдёт мимо платформы (звонок/WhatsApp напрямую, без
// защиты жалобами, без трекинга, без акта). Полностью это не остановить — но можно
// (а) мягко предупредить отправителя в моменте и (б) пометить сообщение для админа,
// чтобы видеть масштаб проблемы и на кого чаще жалуются после таких сообщений.
// Намеренно НЕ блокируем отправку — ложные срабатывания (номер прицепа, вес груза
// с "80" внутри и т.п.) не должны ломать реальную логистическую переписку.

// Казахстанские мобильные номера в любом написании: +7 700 123 45 67, 8(700)1234567,
// 87001234567, 700-123-45-67 и т.п. — код страны (+7/8/7), затем 3+3+2+2 цифры
// с произвольными пробелами/скобками/дефисами между группами.
const PHONE_RE = /(?:\+7|8|7)[\s\-.]?\(?\d{3}\)?[\s\-.]?\d{3}[\s\-.]?\d{2}[\s\-.]?\d{2}\b/;

// Явные упоминания мессенджеров/соцсетей — рус/каз/англ варианты, стемы без строгих
// границ слова (чтобы ловить "телегу", "телеге", "вотсапе" и т.п.).
const MESSENGER_RE = /(whats\s*app|вотсап|ватсап|вацап|telegram|телеграм|телег|viber|вайбер|instagram|инстаграм|инста\b|директ\b|вк\b|vkontakte)/i;

// Попытки "зашифровать" номер словами (семь-семь-ноль, plus seven и т.п.) — базовый
// набор, не претендует на 100% покрытие, обходы всегда будут возможны текстом.
const SPELLED_DIGITS_RE = /\b(ноль|один|два|три|четыре|пять|шесть|семь|восемь|девять)\D{0,3}(ноль|один|два|три|четыре|пять|шесть|семь|восемь|девять)\D{0,3}(ноль|один|два|три|четыре|пять|шесть|семь|восемь|девять)\D{0,3}(ноль|один|два|три|четыре|пять|шесть|семь|восемь|девять)/i;

const EMAIL_RE = /[a-z0-9._%+-]+@[a-z0-9.-]+\.[a-z]{2,}/i;

function detectContactSharing(text) {
  if (!text) return { flagged: false, reasons: [] };
  const reasons = [];
  if (PHONE_RE.test(text)) reasons.push('phone');
  if (MESSENGER_RE.test(text)) reasons.push('messenger');
  if (SPELLED_DIGITS_RE.test(text)) reasons.push('spelled_digits');
  if (EMAIL_RE.test(text)) reasons.push('email');
  return { flagged: reasons.length > 0, reasons };
}

module.exports = { detectContactSharing };
