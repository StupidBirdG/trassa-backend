export function pickTopic(offset = 0) {
  const dayOfYear = Math.floor(
    (Date.now() - Date.UTC(new Date().getUTCFullYear(), 0, 0)) / 86400000
  );
  const idx = (dayOfYear + offset) % 100;
  const factIndex = Math.floor(idx / 10);
  const angleIndex = idx % 10;
  return { fact: FACTS[factIndex], angle: ANGLES[angleIndex], idx, factIndex, angleIndex };
}
